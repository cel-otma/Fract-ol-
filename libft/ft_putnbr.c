#include "libft.h"

void	ft_putnbr(int n)
{
	unsigned int nb1;

	nb1 = n;
	if (n < 0)
	{
		ft_putchar('-');
		nb1 = n * (-1);
	}
	if (nb1 > 9)
	{
		ft_putnbr(nb1 / 10);
		ft_putnbr(nb1 % 10);
	}
	else
	{
		ft_putchar(nb1 + '0');
	}
}
