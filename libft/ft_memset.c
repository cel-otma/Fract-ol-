#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	int			i;
	char		*v;

	i = 0;
	v = (char*)s;
	while (i < (int)n)
	{
		v[i] = c;
		i++;
	}
	return (s);
}
