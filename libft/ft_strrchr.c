char	*ft_strrchr(const void *str, int c)
{
	int i;

	i = 0;
	while (((char*)str)[i] != '\0')
		i++;
	while (i >= 0)
	{
		if (((char *)str)[i] == (char)c)
			return ((char *)str + i);
		i--;
	}
	if (((char *)str)[i] != '\0')
		return (0);
	return (0);
}
