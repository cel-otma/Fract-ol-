#include <string.h>

void	*ft_bzero(void *s, size_t n)
{
	int		i;
	char	*v;

	i = 0;
	v = (char *)s;
	while (i < (int)n)
	{
		v[i] = '\0';
		i++;
	}
	return (s);
}
